# This is an example to demonstrate comments in Python.
# Comments are useful for explaining what your code does, and they're ignored by Python.

# Lines that start with a hash symbol (#) are considered comments.
# This is a comment, so Python will ignore it.
# Anything after the # symbol is also ignored by Python.

# The 'print' function is used here to display the text "I could have code like this."
print("I could have code like this.")  # The comment after this code is also ignored by Python.

# Comments can also be used to disable code temporarily.
# The next line is 'commented out', so it won't be executed by Python.
# print("This won't run.")

# This line will print "This will run." to the console.
print("This will run.")
