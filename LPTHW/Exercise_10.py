# This Python program demonstrates the use of escape sequences in strings.
# Escape sequences allow you to include special characters in strings.

# Using \t for horizontal tab
tabby_cat = "\tI'm tabbed in."

# Using \n for a new line
persian_cat = "I'm split\non a line."

# Using \\ to include a backslash in the string
backslash_cat = "I'm \\ a \\ cat."
tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)